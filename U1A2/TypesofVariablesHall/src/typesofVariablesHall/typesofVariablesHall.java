package typesofVariablesHall;

public class typesofVariablesHall {
    public static void main(String[] args) {
        int intVar;
        double doubleVar;
        String stringVar;
        boolean boolVar;

        intVar = -17;
        doubleVar = 13.6;
        stringVar = "Hello, World!";
        boolVar = true;

        System.out.println("Value of intVar: " + intVar);
        System.out.println("Value of doubleVar: " + doubleVar);
        System.out.println("Value of stringVar: " + stringVar);
        System.out.println("Value of boolVar: " + boolVar);


    }
}
